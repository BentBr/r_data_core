#![deny(clippy::all, clippy::pedantic, clippy::nursery, warnings)]

//! Generates a TypeScript file with validation constants and patterns.
//!
//! These constants are the single source of truth shared between the Rust backend
//! and the Vue3 frontend for form validation.
//!
//! # Usage
//!
//! ```bash
//! cargo run --bin export_validation_constants > fe/src/types/generated/validation.ts
//! ```

use r_data_core_core::validation::{constraints, patterns};

fn main() {
    println!("// AUTO-GENERATED by `rdt generate-ts` -- do not edit");
    println!();
    println!(
        "export const EMAIL_PATTERN = /{}/;",
        patterns::EMAIL_PATTERN
    );
    println!();
    println!(
        "export const USERNAME_MIN_LENGTH = {};",
        constraints::USERNAME_MIN_LENGTH
    );
    println!(
        "export const USERNAME_MAX_LENGTH = {};",
        constraints::USERNAME_MAX_LENGTH
    );
    println!(
        "export const PASSWORD_MIN_LENGTH = {};",
        constraints::PASSWORD_MIN_LENGTH
    );
    println!(
        "export const NAME_MIN_LENGTH = {};",
        constraints::NAME_MIN_LENGTH
    );
    println!(
        "export const API_KEY_NAME_MIN_LENGTH = {};",
        constraints::API_KEY_NAME_MIN_LENGTH
    );
    println!(
        "export const CSV_DELIMITER_LENGTH = {};",
        constraints::CSV_DELIMITER_LENGTH
    );
    println!(
        "export const DSL_STEPS_MIN_COUNT = {};",
        constraints::DSL_STEPS_MIN_COUNT
    );
}
